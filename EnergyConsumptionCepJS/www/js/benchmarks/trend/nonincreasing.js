
function nonIncreasingCepjsRx_minInterval(total){
    var count = 0;
	async.whilst(
		function() { return count < total; },
		function(callback) {
            count++;
            onStartIteration();
            rcepjs.interval(minInterval)
                .pipe(streamController_rcepjs,
                    rcepjs.tumblingTimeWindow(5000),
                    rcepjs.nonIncreasing(['interval event'], 'payload', undefined, 'nonIncreasing event'))
                .subscribe({
                    next: function(value){},
                    error: function(error){},
                    complete: function(){
                        onCompleteIteration(callback, count);
                    }
                });
		},
		onComplete
	);
}

